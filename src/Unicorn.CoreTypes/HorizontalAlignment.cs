namespace Unicorn.CoreTypes
{
    /// <summary>
    /// The horizontal alignment of a drawable object.
    /// </summary>
    public enum HorizontalAlignment
    {
        /// <summary>
        /// Left-aligned.
        /// </summary>
        Left,

        /// <summary>
        /// Centred.
        /// </summary>
        Centred,

        /// <summary>
        /// Right-aligned.
        /// </summary>
        Right,

        /// <summary>
        /// Stretched to fill the available width.
        /// </summary>
        Justified,
    }
}
